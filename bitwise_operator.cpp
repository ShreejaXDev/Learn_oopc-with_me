//bitwise operators


#include<iostream>
using namespace std;

int main()
{
    int a=2, b=3;

    cout<<(a&b)<<endl; //bitwise and
    cout<<(a|b)<<endl;  //bitwise or

    cout<<(a^b)<<endl; // bitwise exor
    cout<<(4<<1)<<endl;//left shift
    cout<<(4>>1)<<endl;//right shift N

}
